// Write a program to get a number and print the number
// For example:

// Test	       Input	Result
// Test Case 1     6        6

import java.util.*;
public class Print_A_Number {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        System.out.print(n);
        sc.close();
    }
}
